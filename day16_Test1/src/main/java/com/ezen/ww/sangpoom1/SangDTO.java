package com.ezen.ww.sangpoom1;

public class SangDTO 
{
	int spnum;
	String spname,sangho,spdate,scontent,spimage;
	int sprice,cnt,mae,su;
	
	public SangDTO() {
		super();
	}
	public int getSpnum() {
		return spnum;
	}
	public void setSpnum(int spnum) {
		this.spnum = spnum;
	}
	public String getSpname() {
		return spname;
	}
	public void setSpname(String spname) {
		this.spname = spname;
	}
	public String getSangho() {
		return sangho;
	}
	public void setSangho(String sangho) {
		this.sangho = sangho;
	}
	public String getSpdate() {
		return spdate;
	}
	public void setSpdate(String spdate) {
		this.spdate = spdate;
	}
	public String getScontent() {
		return scontent;
	}
	public void setScontent(String scontent) {
		this.scontent = scontent;
	}
	public String getSpimage() {
		return spimage;
	}
	public void setSpimage(String spimage) {
		this.spimage = spimage;
	}
	public int getSprice() {
		return sprice;
	}
	public void setSprice(int sprice) {
		this.sprice = sprice;
	}
	public int getCnt() {
		return cnt;
	}
	public void setCnt(int cnt) {
		this.cnt = cnt;
	}
	public int getMae() {
		return mae;
	}
	public void setMae(int mae) {
		this.mae = mae;
	}
	public int getSu() {
		return su;
	}
	public void setSu(int su) {
		this.su = su;
	}
	
	
}
